    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
         DefaultTableModel model = (DefaultTableModel) table.getModel();
        int rowCount = model.getRowCount();

    try {
        FileWriter writer = new FileWriter("C:\\Users\\chris\\OneDrive\\Documents\\NetBeansProjects\\RolandGUI\\src\\RolandPackage\\studentInfo.txt");

        for (int i = 0; i < rowCount; i++) {
            String name = model.getValueAt(i, 0).toString();
            String id = model.getValueAt(i, 1).toString();
            String age = model.getValueAt(i, 2).toString();
            String gpa = model.getValueAt(i, 3).toString();

            // Separate each field with a comma (CSV format)
            writer.write(name + "," + id + "," + age + "," + gpa + "\n");
        }

        writer.close();
        System.out.println("Data successfully saved to clients.txt");

    } catch (Exception ex) {
        ex.printStackTrace();
        }
    }//GEN-LAST:event_formWindowClosing

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        DefaultTableModel model = (DefaultTableModel) table.getModel();
    model.setRowCount(0); // Clear existing data first

    try {
        File file = new File("C:\\Users\\chris\\OneDrive\\Documents\\NetBeansProjects\\RolandGUI\\src\\RolandPackage\\studentInfo.txt");
        if (!file.exists()) return; // If no file yet, skip loading

        BufferedReader reader = new BufferedReader(new FileReader(file));
        String line;

        while ((line = reader.readLine()) != null) {
            // Split line into columns using comma
            String[] data = line.split(",");
            if (data.length == 4) {
                model.addRow(new Object[]{data[0], data[1], data[2], data[3]});
            }
        }

        reader.close();
        System.out.println("Data successfully loaded from clients.txt");

    } catch (Exception ex) {
        ex.printStackTrace();
     }
    }//GEN-LAST:event_formWindowOpened

    private void ftSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ftSearchKeyReleased
        DefaultTableModel ob = (DefaultTableModel) table.getModel();
    TableRowSorter<DefaultTableModel> obj = new TableRowSorter<>(ob);
    table.setRowSorter(obj);
    obj.setRowFilter(RowFilter.regexFilter("(?i)" + ftSearch.getText()));
    }//GEN-LAST:event_ftSearchKeyReleased
    private void saveAllToFile() {
    DefaultTableModel model = (DefaultTableModel) table.getModel();

    try (FileWriter writer = new FileWriter("C:\\Users\\chris\\OneDrive\\Documents\\NetBeansProjects\\RolandGUI\\src\\RolandPackage\\studentInfo.txt", false);
         PrintWriter pWriter = new PrintWriter(writer)) {

        for (int i = 0; i < model.getRowCount(); i++) {
            String id = model.getValueAt(i, 0).toString();
            String name = model.getValueAt(i, 1).toString();
            String age = model.getValueAt(i, 2).toString();
            String grade = model.getValueAt(i, 3).toString();

            pWriter.println(id + "," + name + "," + age + "," + grade);
        }

    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Error saving to file: " + e.getMessage());
    }
}
