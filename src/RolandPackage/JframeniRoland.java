    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
     String id = ftId.getText();
        String name = ftName.getText();
        String age = ftAge.getText();
        String grade = ftGpa.getText();

    if (id.isEmpty() || name.isEmpty() || age.isEmpty() || grade.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Fill out the form");
        return;
    }

    DefaultTableModel model = (DefaultTableModel) table.getModel();
    model.insertRow(0, new Object[]{id, name, age, grade});

    ftId.setText("");
    ftName.setText("");
    ftAge.setText("");
    ftGpa.setText("");

    saveAllToFile(); // Call method to overwrite file   
    
    }//GEN-LAST:event_btnAddActionPerformed

